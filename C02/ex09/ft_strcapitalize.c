char	*ft_strcapitalize(char *str)
{
	int	c;
	int	i;

	c = 0;
	i = 0;
	while (str[c] != '\0')
	{
		if (i == 0 && (str[c] >= 97 && str[c] <= 122))
		{
			str[c] = str[c] - 32;
			i++;
		}
		else if (i > 0 && (str[c] >= 65 && str[c] <= 90))
			str[c] = str[c] + 32;
		else if ((str[c] <= 47) || (str[c] >= 58 && str[c] <= 64)
			|| (str[c] >= 91 && str[c] <= 96) || (str[c] >= 123))
			i = 0;
		else
			i++;
		c++;
	}
	return (str);
}
